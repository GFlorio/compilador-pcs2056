Token* getToken(FILE* fp) {
    int state = 0;
    int next_state = 0;
    char simbolo[64] = {'\0'}; // Simbolos nao podem ter mais de 64 caracteres!
    char* cursor = simbolo;
    int chr = 0;
    Token* token = (Token*) malloc(sizeof Token);

    while ((chr = getc(fp)) != EOF) {
        next_state = matriz_transicao[chr][state];
        if (next_state == -1) {
            token->tipo = -1;
            token->valor = chr;
            return token;
        }
        if (next_state == 0) {
            token->tipo = state;
            token->valor = matriz_saida[chr][state];
            return token;
        }
        *cursor = (char) chr;
        cursor += 1;
    }
    token->tipo = 0;
    token->valor = 0;
    return token;
}
